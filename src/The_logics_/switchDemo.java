package The_logics_;

public class switchDemo {

	public static void main(String[] args) {
		
		char choise='A';
		
		switch(choise)
		{
		case 'A':System.out.println("INDIA");
		break;
		case 'B':System.out.println("AUSTRALIA");
		break;
		case 'C':System.out.println("ENGLAND");
		break;
		}
		
		

	}

}
