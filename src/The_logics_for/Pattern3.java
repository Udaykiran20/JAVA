package The_logics_for;

public class Pattern3 {

	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++) {
			for(int k=1;k<=i;k++) {
				System.out.print(" 0");
			}
			System.out.println();
		}
		for(int i=4;i>0;i--) {
			for(int k=1;k<=i;k++) {
				System.out.print(" 0");
			}
			System.out.println();
		}

	}

}
